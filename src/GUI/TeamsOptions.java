    /**
     * Opens a dialog to select a team and displays the members of that team.
     * Shows an error message if the team has no manager or players.
     *
     * @author Maryam Mohsen, Fatima Khamis, Zahra Husain
     */
    private void btnDispTeamMembersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDispTeamMembersActionPerformed

        JComboBox<String> comboBox = new JComboBox<>();
        ArrayList<Team> teamsList = SportsLeagueSystem.getTeamsList();
        if (teamsList.size() == 0) {
            JOptionPane.showMessageDialog(this, "No teams found!");
        } else {
            for (Team team : teamsList) {
                comboBox.addItem("Name: " + team.getTeamName() + " - ID: " + team.getTeamId());
            }
            int choice = JOptionPane.showConfirmDialog(this, comboBox, "Select Team", JOptionPane.OK_CANCEL_OPTION);
            if (choice == JOptionPane.OK_OPTION) { // If OK button is clicked
                String selectedTeamString = (String) comboBox.getSelectedItem();
                // Extract team ID from the selected item string
                Integer teamId = Integer.parseInt(selectedTeamString.split("ID: ")[1].trim());
                if (teamId != null) {
                    Team tempTeam = SportsLeagueSystem.searchTeam(teamId);
                    //check if the team has members (a manager and players) 
                    if (tempTeam.getTeamPlayers().size() == 0 && tempTeam.getTeamManager() == null) {
                        JOptionPane.showMessageDialog(this, "The team does not have a manager or players!");
                    } else {
                        DisplayTeamMembers dispTeamMembers = new DisplayTeamMembers(tempTeam, this);
                        dispTeamMembers.setVisible(true);
                        this.setVisible(false); // Hide the current window 
                    }

                }
            }

        }

    }//GEN-LAST:event_btnDispTeamMembersActionPerformed
    /**
     * Opens a dialog to select a team and a player to designate as team
     * captain. Shows an error message if no players are found for the team.
     *
     * @author Maryam Mohsen, Fatima Khamis, Zahra Husain
     */
    private void btnDesignatePlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesignatePlayerActionPerformed

        // TODO add your handling code here:
        JComboBox comboBox = new JComboBox();
        ArrayList<Team> teamsList = SportsLeagueSystem.getTeamsList();
        if (teamsList.size() == 0) {
            JOptionPane.showMessageDialog(this, "No teams found!");
        } else {
            for (Team team : teamsList) {
                comboBox.addItem("Name: " + team.getTeamName() + " - ID: " + team.getTeamId());
            }
            int choice = JOptionPane.showConfirmDialog(this, comboBox, "Select Team", JOptionPane.OK_CANCEL_OPTION);
            if (choice == JOptionPane.OK_OPTION) { // If OK button is clicked
                String selectedTeam = (String) comboBox.getSelectedItem();
                int teamId = Integer.parseInt(selectedTeam.substring(selectedTeam.lastIndexOf("ID: ") + 4));

                // Check if teamId is not null
                if (selectedTeam != null) {
                    JComboBox cb = new JComboBox();
                    ArrayList<Player> playersList = SportsLeagueSystem.searchTeam(teamId).getTeamPlayers();
                    if (playersList.size() == 0) {
                        JOptionPane.showMessageDialog(this, "No players found for this team!");
                    } else {
                        for (Player player : playersList) {
                            cb.addItem("Name: " + player.getMemberName() + " - ID: " + player.getMemberId());
                        }
                        int choice1 = JOptionPane.showConfirmDialog(this, cb, "Select Player", JOptionPane.OK_CANCEL_OPTION);
                        if (choice1 == JOptionPane.OK_OPTION) { // If OK button is clicked
                            String selectedPlayer = (String) cb.getSelectedItem();
                            int playerId = Integer.parseInt(selectedPlayer.substring(selectedPlayer.lastIndexOf("ID: ") + 4));
                            //check if the player is already the team captain 
                            Player tempPlayer = (Player) SportsLeagueSystem.searchMember(playerId);
                            if (tempPlayer.isCaptain() == true) {
                                JOptionPane.showMessageDialog(this, "The player is already the team captain!");
                            } else {
                                //if the player is not the current team captain, call the method to designate the player
                                SportsLeagueSystem.designatePlayerAsCaptain(teamId, playerId);
                                JOptionPane.showMessageDialog(this, "The player has been designated successfully!");
                            }

                        }
                    }
                }

            }
        }
    }//GEN-LAST:event_btnDesignatePlayerActionPerformed
    /**
     * Opens a dialog to select a team for deletion. Deletes the selected team
     * after confirmation and shows a success message.
     *
     * @author Maryam Mohsen, Fatima Khamis, Zahra Husain
     */
    private void btnDeleteTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteTeamActionPerformed
        // TODO add your handling code here:
        JComboBox<String> comboBox = new JComboBox<>();
        ArrayList<Team> teamsList = SportsLeagueSystem.getTeamsList();
        if (teamsList.size() == 0) {
            JOptionPane.showMessageDialog(this, "No teams found!");
        } else {
            for (Team team : teamsList) {
                comboBox.addItem("Name: " + team.getTeamName() + " - ID: " + team.getTeamId());
            }
            int choice = JOptionPane.showConfirmDialog(this, comboBox, "Select Team", JOptionPane.OK_CANCEL_OPTION);
            if (choice == JOptionPane.OK_OPTION) {
                String selectedItem = (String) comboBox.getSelectedItem();
                if (selectedItem != null) {
                    // Extract team ID only the number from the selected item
                    String[] parts = selectedItem.split("- ID:"); // Split the string based on "- ID:"

                    if (parts.length == 2) {
                        String teamIdStr = parts[1].trim(); // Get the second part and remove leading/trailing spaces
                        try {
                            int teamId = Integer.parseInt(teamIdStr); // Parse the team ID
                            System.out.println("team ID ---: " + teamId);
                            //confirmation message 
                            int confirmationResult = JOptionPane.showConfirmDialog(this, "Are you certain you want to delete this team?");

                            if (confirmationResult == JOptionPane.YES_OPTION) {
                                // User clicked Yes
                                //delete the team
                                SportsLeagueSystem.deleteTeam(teamId);
                                //display success message
                                JOptionPane.showMessageDialog(this, "The team has been deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                            }

                        } catch (NumberFormatException e) {
                            // Handle the case where parsing fails
                            e.printStackTrace(); // or handle it in another appropriate way
                        }
                    }

                }

            }

        }

    }//GEN-LAST:event_btnDeleteTeamActionPerformed
