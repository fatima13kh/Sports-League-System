    /**
     * Opens a dialog to select a team for alteration and opens the Alter Team
     * Form. Hides the current window after selecting the team.
     *
     * @author Maryam Mohsen, Fatima Khamis, Zahra Husain
     */
    private void btnAlterTeamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterTeamActionPerformed
        // TODO add your handling code here:
        JComboBox comboBox = new JComboBox();
        ArrayList<Team> teamsList = SportsLeagueSystem.getTeamsList();
        if (teamsList.size() == 0) {
            JOptionPane.showMessageDialog(this, "No teams found!");
        } else {
            for (Team team : teamsList) {
                comboBox.addItem("Name: " + team.getTeamName() + " - ID: " + team.getTeamId());
            }
            int choice = JOptionPane.showConfirmDialog(this, comboBox, "Select Team", JOptionPane.OK_CANCEL_OPTION);
            if (choice == JOptionPane.CANCEL_OPTION || choice == JOptionPane.CLOSED_OPTION) {// if clicked cancel or close
                this.setVisible(true); //display current window and hide the pop up
            } else {
                String selectedTeamString = (String) comboBox.getSelectedItem();
                Integer teamId = Integer.parseInt(selectedTeamString.split("ID: ")[1].trim());
                if (teamId != null) {
                    Team tempTeam = SportsLeagueSystem.searchTeam(teamId);
                    AlterTeamForm alterTeam = new AlterTeamForm(this, tempTeam);
                    alterTeam.setVisible(true);
                    dispose();

                }
            }
        }

    }//GEN-LAST:event_btnAlterTeamActionPerformed
