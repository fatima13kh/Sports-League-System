    /**
     * Handles the event when the submit button is clicked. Performs validation
     * checks on manager details and alters them if valid.
     *
     * @author Maryam Mohsen, Fatima Khamis, Zahra Husain
     * @param evt The ActionEvent triggering this method.
     */
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

//sets the text fields to null 
        boolean submit = true;
        nameLblErr.setText(null);
        addressLblErr.setText(null);
        dateLblErr.setText(null);
        nationalityLblErr.setText(null);
        salaryLblErr.setText(null);
        bonusLblErr.setText(null);
        qualificationsLblErr.setText(null);
//creates variables for the text inserted
        String managerName = managerNameText.getText();
        String managerAddress = managerAddressText.getText();
        String managerNationality = nationalityText.getText();
        String managerSalary = managerSalaryText.getText();
        String managerBonus = managerBonusText.getText();
        String managerQualifications = managerQualificationsText.getText();
        double salary = 0.0;
        double bonus = 0.0;
//checks that the data entered is not empty and within certain length
        if (managerName.isEmpty() || managerName.length() < 3) {
            nameLblErr.setText(managerName.isEmpty() ? "Manager Name cannot be blank" : "Manager Name must be at least 3 characters");
            submit = false;
        }

        if (managerAddress.isEmpty() || managerAddress.length() < 10) {
            addressLblErr.setText(managerAddress.isEmpty() ? "Manager Address cannot be blank" : "Manager Address must be at least 10 characters");
            submit = false;
        }

        if (managerNationality.isEmpty() || managerNationality.length() < 3) {
            nationalityLblErr.setText(managerNationality.isEmpty() ? "Manager Nationality cannot be blank" : "Nationality must be at least 3 characters");
            submit = false;
        }

        if (managerQualifications.isEmpty() || managerQualifications.length() < 8) {
            qualificationsLblErr.setText(managerQualifications.isEmpty() ? "Manager Qualification cannot be blank" : "Qualification must be at least 8 characters");
            submit = false;
        }

        if (managerSalary.isEmpty()) {
            salaryLblErr.setText("Manager Salary cannot be blank");
            submit = false;
        } else {
            try {
                salary = Double.parseDouble(managerSalary);
                if (salary < 400) {
                    salaryLblErr.setText("Manager Salary must be at least 400");
                    submit = false;
                }
            } catch (NumberFormatException e) {
                salaryLblErr.setText("Manager Salary must be a number");
                submit = false;
            }
        }

        if (managerBonus.isEmpty()) {
            bonusLblErr.setText("Manager Bonus cannot be blank");
            submit = false;
        } else {
            try {
                bonus = Double.parseDouble(managerBonus);
                if (bonus < 0) {
                    bonusLblErr.setText("Manager Bonus cannot be negative");
                    submit = false;
                }
            } catch (NumberFormatException e) {
                bonusLblErr.setText("Manager Bonus must be a number");
                submit = false;
            }
        }

        Date dob = dateOfBirthChooser.getDate();
        if (dob == null) {
            dateLblErr.setText("Manager Date of Birth cannot be blank");
            submit = false;
        } else {
            Calendar currentDate = Calendar.getInstance();
            currentDate.add(Calendar.YEAR, -15);
            Calendar dobCalendar = Calendar.getInstance();
            dobCalendar.setTime(dob);
            if (!dobCalendar.before(currentDate)) {
                submit = false;
                dateLblErr.setText("The manager is not more than 15 years old.");
            }
        }
// Check if manager name or manager nationality contains numbers
        Pattern pattern = Pattern.compile(".*\\d+.*");
        Matcher matcherManagerName = pattern.matcher(managerName);
        Matcher matcherManagerNationality = pattern.matcher(managerNationality);

        if (matcherManagerName.find()) {
            nameLblErr.setText("Manager Name cannot contain numbers");
            submit = false;
        }

        if (matcherManagerNationality.find()) {
            nationalityLblErr.setText("Manager Nationality cannot contain numbers");
            submit = false;
        }

        if (submit) {
            SimpleDateFormat sdf = new SimpleDateFormat("d/M/yyy");
            String dobString = sdf.format(dob);
            SportsLeagueSystem.alterManagerMember(tempManager.getMemberId(), bonus, managerQualifications, managerName, managerAddress, dobString, managerNationality, salary);
            JOptionPane.showMessageDialog(this, "The manager details have been altered successfully");
            MembersOptions membersOptions = new MembersOptions();
            membersOptions.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

