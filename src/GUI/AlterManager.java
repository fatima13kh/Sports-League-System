    //display the stored infromation of the chosen manager in the text fields
    private void alterManagerWindowOpened() throws ParseException {
        managerNameText.setText(tempManager.getMemberName());
        managerAddressText.setText(tempManager.getMemberAddress());

        String dobString = tempManager.getDateOfBirth();
        Date date = new SimpleDateFormat("d/M/yyy").parse(dobString);
        dateOfBirthChooser.setDate(date);

        nationalityText.setText(tempManager.getNationality());
        String salary = Double.toString(tempManager.getYearlySalary());
        managerSalaryText.setText(salary);
        String bonus = Double.toString(tempManager.getBonusPercentage());
        managerBonusText.setText(bonus);
        managerQualificationsText.setText(tempManager.getCoachingQualifications());

    }

